package pe.com.sistemaera.core.bean;

import java.io.Serializable;
import java.util.List;

import org.springframework.stereotype.Component;

@Component
public class BUsuarioBean implements Serializable {

	private static final long serialVersionUID = 1L;
	private long id;
	private String login;
	private String correoElectronico;
	private String password;
	private String apellidoPaterno;
	private String apellidoMaterno;
	private String nombre;
	private List<BOpcionBean> listaOpciones;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getLogin() {
		return login;
	}

	public void setLogin(String login) {
		this.login = login;
	}

	public String getCorreoElectronico() {
		return correoElectronico;
	}

	public void setCorreoElectronico(String correoElectronico) {
		this.correoElectronico = correoElectronico;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getApellidoPaterno() {
		return apellidoPaterno;
	}

	public void setApellidoPaterno(String apellidoPaterno) {
		this.apellidoPaterno = apellidoPaterno;
	}

	public String getApellidoMaterno() {
		return apellidoMaterno;
	}

	public void setApellidoMaterno(String apellidoMaterno) {
		this.apellidoMaterno = apellidoMaterno;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public List<BOpcionBean> getListaOpciones() {
		return listaOpciones;
	}

	public void setListaOpciones(List<BOpcionBean> listaOpciones) {
		this.listaOpciones = listaOpciones;
	}

}
